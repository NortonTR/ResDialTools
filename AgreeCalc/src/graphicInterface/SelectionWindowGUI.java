    private void Botao_avancarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Botao_avancarActionPerformed
        // TODO add your handling code here:
        File novo;
        if(Texto_caminho.getText()!=null){
            if(new File(Texto_caminho.getText()).exists()){
                RUser r = RUser.getInstance();
                r.internacionaliza(local.getLanguage());
                
                boolean continuar = true;
                if(parent.equals(FerramentaGUI.class.getCanonicalName())){
                    continuar = !MainGUI.showForgetCorpusMsg; //se nao for pra mostrar a tela, sempre continua
                    JCheckBox check = new JCheckBox(bundle.getString("DontShowAgain"));
                    if(MainGUI.showForgetCorpusMsg){
                        int diagResp = JOptionPane.showOptionDialog(null,
                                        bundle.getString("QuestionForgetCorpus"),
                                        bundle.getString("Attention"),
                                        JOptionPane.YES_NO_OPTION,
                                        JOptionPane.WARNING_MESSAGE,
                                        null,
                                        new Object[]{check,bundle.getString("Yes"),bundle.getString("No")},
                                        bundle.getString("No"));
                        if(check.isSelected()){
                            MainGUI.showForgetCorpusMsg=false;
                            MainGUI.gravarConfigs();
                        }
                        if(diagResp == 1) //posição do Yes
                            continuar=true;
                        else continuar=false;
                    }
                }
                
                if(continuar){
                    corpusReset=true;
                    if(parent.equals(FerramentaGUI.class.getCanonicalName())) 
                        Botao_voltar.setEnabled(false);
                    r.reset();

                    AgreeCalcWorker worker = new CorpusReader(bundle, Texto_caminho.getText());
                    DialogoProgresso diag = new DialogoProgresso(this.getRootPane(),worker,bundle);
                    diag.setVisible(true);

                    if(diag.getResponse().getClass().equals(Boolean.class)){
                        boolean resp =(boolean) diag.getResponse();
                        if(resp){
                            this.dispose();
                            FerramentaGUI next = FerramentaGUI.getInstance();
                            next.internacionaliza(local.getLanguage());
                            next.exibirInfos();
                            //next.setSelectedTab(0);
                            next.setVisible(true);
                        }
                    }
                }
                    
            }else{
                //nao existe o arquivo selecionado..
            }
        }
    }//GEN-LAST:event_Botao_avancarActionPerformed

