    private void Botao_voltarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Botao_voltarActionPerformed
        if(parent.equals(MainGUI.class.getCanonicalName())){ //se a janela pai foi a tela de apresentação
            this.dispose();
            MainGUI voltar = MainGUI.getInstance();
            voltar.setVisible(true);
        } else if(!corpusReset && parent.equals(FerramentaGUI.class.getCanonicalName())){ //se a janela pai foi a tela principal
            this.dispose();
            FerramentaGUI voltar = FerramentaGUI.getInstance();
            voltar.setVisible(true);
        }
    }//GEN-LAST:event_Botao_voltarActionPerformed

