    private void Botao_SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Botao_SalvarActionPerformed
        /*
         *  Salva as configurações da Opcao
         *      Caso seja uma Edição atualiza o Elemento na Lista
         *      Caso contrario apenas adiciona este na Lista
         */
        
        ResourceBundle bundle = ResourceBundle.getBundle("MetaAnn.Idioma.JFrameOpcao.JFrameOpcao", locale);
        
        String valorUsuario = Text_Titulo_Usuario.getText().toUpperCase();
        String valorAnotacao = Text_Titulo_Anotacao.getText().toUpperCase();
        
        if(valorUsuario.trim().length()==0){
            JOptionPane.showMessageDialog(this, bundle.getString("erroNomeOpcao"), bundle.getString("erroCampoObrig"), JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(valorAnotacao.trim().length()==0){
            JOptionPane.showMessageDialog(this, bundle.getString("erroValorOpcao"), bundle.getString("erroCampoObrig"), JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        boolean completo;
        Opcao temp = new Opcao();
        
            temp.setValorUsuario(valorUsuario);
            temp.setValorAnotacao(valorAnotacao);
        
        if(EDICAO){
            completo = JFRAME_CATEGORIA.EditaOpcao(temp, opcao_editada);
        }
        
        else{
            completo = JFRAME_CATEGORIA.AdicionaOpcao(temp);
        }
        
        if(completo){
            JFRAME_CATEGORIA.setEnabled(true);
            this.dispose();
        }
        
        JFRAME_CATEGORIA.controlarBotoes();
    }//GEN-LAST:event_Botao_SalvarActionPerformed

