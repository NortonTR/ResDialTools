    private void internacionaliza(){
    
        ResourceBundle bundle = ResourceBundle.getBundle("MetaAnn.Idioma.JFrameOpcao.JFrameOpcao", locale);
        
        if(!EDICAO)
            setTitle("MetaAnn - " + bundle.getString("adicionarOpcao"));
        else
            setTitle("MetaAnn - " + bundle.getString("editarOpcao"));
        
        Botao_Cancelar.setText(bundle.getString("cancelar"));
        Botao_Salvar.setText(bundle.getString("salvar"));
        Label_Titulo_Usuario.setText(bundle.getString("mensagem1"));
        Label_Titulo_Anotacao.setText(bundle.getString("mensagem2"));
        
    }
    
